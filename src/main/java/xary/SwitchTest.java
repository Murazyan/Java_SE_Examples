package xary;

public class SwitchTest {
    public static void main(String[] args) {
        byte k = 0;

        switch(k) {
            default:
                int h = 3;
                System.out.println(h);
                break;
            case 0:
                int a = 7;
                System.out.println(a);
            case 1:
                int b = 11;
                System.out.println(b);
        }
    }
}


